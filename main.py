user_age = int(input("What is your age? "))

print(f"Your age, {user_age}, is equal to {user_age * 12} months.")
print(f"Your age, {user_age}, is equal to {user_age * 365} days.")
print(f"Your age, {user_age}, is equal to {user_age * 365 * 24} hours")
print(f"Your age, {user_age}, is equal to {user_age * 365 * 24 * 60} minutes")
print(f"Your age, {user_age}, is equal to {user_age * 365 * 24 * 60 * 60} seconds")